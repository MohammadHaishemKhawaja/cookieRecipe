# How many cookies do you want to make?
# This recipe makes 48 cookies

x = input("How many cookies do you want to make?")




sugar = (float(x))*1.5
butter = (float(x))*1
flour = (float(x))*2.75
print(f"You need {sugar} cups of sugar, {butter} cups of butter, and {flour} cups of flour")
                
